export function detectInitialLanguage() {
  const saved = localStorage.getItem('tron-whale-radar-lang');
  if (saved) {
    return normalizeLanguage(saved);
  }

  return normalizeLanguage(navigator.language || 'en');
}

export function normalizeLanguage(value) {
  return String(value || '').toLowerCase().startsWith('zh') ? 'zh-CN' : 'en';
}

export function isChinese(value) {
  return normalizeLanguage(value) === 'zh-CN';
}

export function t(language, key, vars = {}) {
  const locale = normalizeLanguage(language);
  const template = TEXT[locale]?.[key] ?? TEXT.en[key] ?? key;
  return Object.entries(vars).reduce(
    (result, [name, value]) => result.replaceAll(`{${name}}`, String(value)),
    template
  );
}

export function localeFor(language) {
  return isChinese(language) ? 'zh-CN' : 'en-US';
}
